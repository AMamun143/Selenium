package javaLessons;

public class WhileLoop {

	public static void main(String[] args) {
		int a = 10;
		int b = 0;
		
		while (a >= 1){
			System.out.println(a);
			a--;
		}
		
		System.out.println("---------------------------");
		do {
			System.out.println(b);
			b++;
		} while (b <= 10);

	}

}
